package light.img.service;

public class ImgVo {
	private int productNum;
	private String imgList;
	private String imgDetail;
	private String imgAccount;
	private String imgInfo;
	private String imgSort;
	
	public int getProductNum() {
		return productNum;
	}
	public void setProductNum(int productNum) {
		this.productNum = productNum;
	}
	public String getImgList() {
		return imgList;
	}
	public void setImgList(String imgList) {
		this.imgList = imgList;
	}
	public String getImgDetail() {
		return imgDetail;
	}
	public void setImgDetail(String imgDetail) {
		this.imgDetail = imgDetail;
	}
	public String getImgAccount() {
		return imgAccount;
	}
	public void setImgAccount(String imgAccount) {
		this.imgAccount = imgAccount;
	}
	public String getImgInfo() {
		return imgInfo;
	}
	public void setImgInfo(String imgInfo) {
		this.imgInfo = imgInfo;
	}
	public String getImgSort() {
		return imgSort;
	}
	public void setImgSort(String imgSort) {
		this.imgSort = imgSort;
	}
}
